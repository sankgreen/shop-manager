public class Proprieta {
	private String valore;
	private String chiave;

	public Proprieta(String chiave, String valore) {
		super();
		this.valore = valore;
		this.chiave = chiave;
	}

	public String getValore() {
		return valore;
	}

	public void setValore(String valore) {
		this.valore = valore;
	}

}
